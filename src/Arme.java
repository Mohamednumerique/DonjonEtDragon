public class Arme {

    // Attributs

    private String nom;
    private  int force;

    // Les constructeurs


    public Arme(String mNom, int mForce){
        this.nom = mNom;
        this.force = mForce;

    }

    //geter & setter


    public String getNom(){
        return this.nom;
    }

    public int getForce(){
        return this.force;
    }
















}
